const dicts: Record<Locale, Dict> = { en, zh };

interface LocaleState {
  locale: Locale;
  setLocale: (l: Locale) => void;
}

function initialLocale(): Locale {
  const saved = localStorage.getItem("acta.locale");
  return saved === "en" || saved === "zh" ? saved : "zh"; // default Chinese
}

export const useLocale = create<LocaleState>((set) => ({
  locale: initialLocale(),
  setLocale: (l) => {
    localStorage.setItem("acta.locale", l);
    set({ locale: l });
  },
}));

export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>,
): string {
  let s = dicts[locale][key] ?? dicts.en[key] ?? key;
  if (vars)
    for (const [k, v] of Object.entries(vars))
      s = s.replace(`{${k}}`, String(v));
  return s;
}

export type TFn = (key: string, vars?: Record<string, string | number>) => string;

/** Hook: returns a translator bound to the current locale plus the switcher. */
export function useI18n(): { t: TFn; locale: Locale; setLocale: (l: Locale) => void } {
  const locale = useLocale((s) => s.locale);
  const setLocale = useLocale((s) => s.setLocale);
  const t: TFn = (key, vars) => translate(locale, key, vars);
  return { t, locale, setLocale };
}
